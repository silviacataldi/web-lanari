				        <li><strong>Espirometría y curva flujo-volumen c/s broncodilatadores<br /></strong>Es el estudio más común que se hace en el Laboratorio y permite conocer en parte el tamaño de los pulmones, la fuerza de los músculos respiratorios y el estado de los bronquios. Consiste en inspirar bien profundo y soplar todo el aire de los pulmones a través de un tubo conectado a una computadora. Es un estudio no invasivo (no requiere la colocación de tubos, catéteres u otro dispositivo), por lo que no duele y no necesita anestesia. La prueba se debe repetir al menos 3 veces, según las instrucciones del técnico. En algunos casos, luego de soplar tres veces, se le da al paciente un broncodilatador en aerosol y, después de unos minutos, se repite la prueba.</li>				
						<li><strong>Volúmenes pulmonares (método de lavado de nitrógeno)<br /></strong>Estudio no invasivo que permite saber el tamaño total de los pulmones. Es más preciso que la espirometría. Para ello se debe respirar tranquilo durante 2 a 3 minutos a través de una boquilla.</li>
						<li><strong>DLCO (difusión de monóxido de carbono)<br /></strong>Otro estudio no invasivo. Permite conocer la capacidad de los pulmones de llevar oxígeno a la sangre. Conectado a una boquilla, se sopla todo el aire de los pulmones, para luego hacer una inspiración profunda y retener el aire, largándolo lentamente.</li>
						<li><strong>Caminata de 6 minutos (6MWT)<br /></strong>Consiste en hacer caminar al paciente a su propio ritmo a lo largo de un pasillo de 30 metros, ida y vuelta. Se debe determinar saturometría antes y después de caminar. Si el paciente se siente fatigado, puede parar la prueba en cualquier momento.</li>
						<li><strong>Saturometría de pulso (SpO2)<br /></strong>Es la manera no invasiva de estimar el nivel de oxígeno en sangre. Para ello se coloca un dispositivo de plástico en forma de pinza, con una luz roja en su interior, que registra el porcentaje de oxígeno. No pincha ni aprieta el dedo. Si el estudio deja dudas, se debe confirmar con una muestra de sangre.</li>
						<li><strong>Flujo espiratorio pico (PEF)<br /></strong>Estudio simple y de bajo costo para evaluar la capacidad de toser de los pacientes y el grado de obstrucción de sus bronquios. Se le pide al paciente que inspire profundamente y que exhale de forma rápida y fuerte el aire contenido dentro de sus pulmones</li>
						<li><strong>Presiones estáticas máximas: PiMax, PeMax y Sniff nasal<br /></strong>También es un estudio no invasivo y sirve para conocer la fuerza de los músculos respiratorios. Consiste en aspirar (PiMax) o soplar (PeMax) con fuerza a través de una boquilla que es tapada durante un segundo, permitiendo luego el pasaje de aire.</li>
