						<li><strong>Capnografia (PetCO2)<br /></strong>Sirve para conocer la cantidad aproximada de dióxido de carbono, que es el deshecho de las células del cuerpo y que normalmente se elimina por la respiración. Se le coloca al paciente una bigotera sobre el labio superior, que se conecta a un aparato que lee la cantidad de dióxido de carbono. Si el estudio deja dudas, se debe confirmar con una muestra de sangre.</li>
						<li><strong>Gases en sangre arterial arterial<br /></strong>Es uno de los pocos estudios invasivos que se hacen en el Laboratorio. Consiste en tomar una muestra de sangre, normalmente de la muñeca, para su análisis. El resultado se recibe en 15 minutos y permite conocer con exactitud la capacidad de los pulmones de captar oxígeno y de eliminar dióxido de carbono.</li>
						<li><strong>Prueba de ejercicio cardiopulmonar/ consumo de oxígeno (VO2)<br /></strong>Estudio que permite conocer el funcionamiento global del sistema cardiopulmonar. En pacientes se usa para evaluaciones pre-quirúrgicas, en el estudio de disnea de causa no aclarada y en la valoración del estado previo a programas de rehabilitación. En deportistas, permite evaluar su rendimiento deportivo y la detección de limitaciones al rendimiento no detectadas con las pruebas convencionales. El sujeto debe caminar o correr sobre una cinta sin fin, conectado a una máscara, a velocidades e inclinaciones acordes a protocolos de trabajo adaptados para cada caso. En grupos de atletas, permite elaborar informes comparativos de desempeño fisiológico, de manera de personalizar el entrenamiento dentro del grupo.</li>
						<li><strong>Prueba de ejercicio cardiopulmonar con protocolo para miopatías metabólicas</strong><br />Similar al anterior, se complementa con la extracción de muestras sucesivas de sangre extraídas a través de un catéter en el antebrazo. Permite detectar trastornos en el metabolismo muscular o mitocondrial, que pueden causar fatiga precoz y/o dolores musculares.</li>
						<li><strong>Calorimetría indirecta<br /></strong>Estudio que estima el consumo metabólico en reposo, de utilidad en programar dietas en pacientes con trastornos endocrinológicos, personas con problemas de sobre o bajo peso y en atletas de elite. Es no invasivo</li>
						<li><strong>Prueba hipóxica de simulación de altura (HAST)<br /></strong>Con el paciente sentado, se lo hace respirar a través de una máscara una mezcla gaseosa que simula las condiciones en la cabina de un avión comercial. Durante la prueba se monitorean variables respiratorias y cardiológicas que permiten determinar si un paciente tiene riesgo de tener complicaciones durante un vuelo comercial, y si requerirá oxígeno durante el viaje.</li>
						<li>
						<p><strong>Prueba de respuesta ventilatoria a la hipoxia<br /></strong>Estudio destinado a montañistas y viajeros a la altura. Permite detectar problemas cardiorrespiratorios mediante la exposición a una mezcla hipóxica y la evaluación de su comportamiento fisiológico.</p>
						</li>
						<li><strong>Titulación de oxígeno para uso domiciliario<br /></strong>Consiste en ajustar el flujo de oxígeno según lo que el paciente necesite. Para ello no es necesario sacar sangre. Se hace mediante la saturometría de pulso.</li>
						<li><strong>Cálculo de shunt con oxígeno al 100 %<br /></strong>Se usa para buscar algunas enfermedades cardíacas o pulmonares que bajan el oxígeno en sangre. Para ello se toman 2 muestras de gases arteriales, sin y con una máscara con oxígeno puro.</li>
						<li><strong>Determinación de afinidad de la hemoglobina (P50)<br /></strong>Se usa en algunas enfermedades de la sangre. Se hace analizando una muestra de sangre extraída de una vena del antebrazo.</li>
						<li><strong>Prueba de broncoprovocación con ejercicio<br /></strong>Permite determinar qué pacientes tienen riesgo de padecer broncospasmo al realizar ejercicio.</li>
						<li><strong>Esputo inducido<br /></strong>Es una técnica que permite obtener secreciones bronquiales cuando estas son escasas. Para ello se hace nebulizar al paciente con diferentes soluciones, hasta que puede toser y escupir secreciones que se puedan analizar.</li>
