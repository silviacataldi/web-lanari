				<h4>¿Qué son los Cuidados Paliativos?</h4>
				<p>Los Cuidados Paliativos los define la Organización Mundial de la Salud en 2002 como &laquo;un abordaje que mejora la calidad de vida de los pacientes y sus familiares frente a los problemas asociados a enfermedades que amenazan la vida, a través de la prevención y alivio del sufrimiento, mediante la temprana identificación, evaluación precisa y resolución del dolor y otros problemas físicos, psicosociales y espirituales. Su implementación requiere el abordaje en equipo interdisciplinario para identificar las necesidades de los pacientes y sus familias&raquo;.</p>
				<h4>¿Quienes pueden beneficiarse con estos cuidados?</h4>
				<p>Son aplicables tempranamente en el curso de la enfermedad, en conjunto con otros tratamientos que intentan prolongar la vida e incluyen aquellos estudios necesarios para comprender y manejar mejor las complicaciones clínicas que provocan sufrimiento. Los Cuidados Paliativos proponen cuidar más allá de curar.</p>
				<h4>¿Cuáles son los principios fundamentales de los Cuidados Paliativos?</h4>
				<ul class="lista">
					<li>Reafirmar la importancia de la vida, considerando a la muerte como un proceso natural.</li>
					<li>Establecer una estrategia de asistencia que no acelere la llegada de la muerte, ni tampoco la posponga artificialmente.</li>
					<li>Proporcionar alivio del dolor y de otros síntomas angustiantes.</li>
					<li>Integrar los aspectos psicológicos y espirituales del tratamiento del paciente y su familia.</li>
					<li>Ofrecer un sistema de apoyo para ayudar a las personas enfermas a llevar una vida lo más activa posible hasta el final.</li>
					<li>Extender este apoyo a los familiares, facilitando que puedan afrontar la enfermedad del ser querido y sobrellevar el período de duelo.</li>
				</ul>
