			<h3>Anticuerpos: Anti 21 hidroxilasa séricos</h3>
			<h3>¿Que significado tiene su análisis?</h3>
			<p>Los anticuerpos anti 21 hidroxilasa séricos son específicos de la adrenalitis autoinmune, están presentes en 3 de los 4 tipos del síndrome poliendocrino autoinmune. Este síndrome se asocia a enfermedades autoinmunes endocrinas y no endocrinas. Algunas de ellas son:: tiroiditis, diabetes mellitus tipo I; vitiligo, enfermedad celíaca, lupus, artritis reumatoidea, miastenia gravis, entre otras. El componente hereditario está asociado a un HLA específico, y su análisis puede establecer la susceptibilidad genética para este síndrome en familiares.</p>
			<p>Ante los primeros signos y/o síntomas relacionados con las enfermedades autoinmunes citadas, el dosaje de anti 21 hidroxilasa , orienta al médico tratante para tomar la conducta adecuada en el diagnostico precoz y seguimiento del paciente con el síndrome poliendocrino autoinmune. Ya que la presencia del anticuerpo anti 21 hidroxilasa implica alto riesgo de progresión a insuficiencia suprarrenal.</p>
			<p><strong>RECIBIMOS DERIVACIONES y SOMOS REFERENTES PARA ESTOS ESTUDIOS. SUGERIMOS CONTACTARSE A:</strong><a href="mailto:roveto.silvana@lanari.fmed.uba.ar">roveto.silvana@lanari.uba.ar</a></p>
			<h3>Hormona anti Mülleriana –AMH SÉRICA</h3>
			<h3>¿Que significado tiene dosarla?</h3>
			<p>La hormona antimülleriana es un biomarcador sérico de la función ovárica que está adquiriendo interés como predictor de tiempo de inicio de la menopausia y marcador de reserva ovárica , siendo actualmente uno de los más importantes y utilizados para tal fin. En la mujer es producida por las células de la granulosa del ovario desde la semana 36 de gestación hasta la menopausia. Los niveles séricos declinan con la edad y llegan a ser muy bajos o indetectables incluso hasta 5 años antes que suceda el último período menstrual</p>
			<p>Ultimas investigaciones sugieren la medición de AMH como marcador de reserva ovárica, en aquellas pacientes en edad fértil, que por algún tratamiento médico, vean afectada su capacidad reproductiva . Se puede realizar el estudio de AMH , aún bajo tratamiento con anticonceptivos, y al no estar regulado por FSH, puede realizarse su medición en cualquier día del ciclo menstrual</p>
