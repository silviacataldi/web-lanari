<h3>BIOGRAFÍA DR. ALFREDO LANARI</h3>
<img src="../../img/lanari1.jpg" class="float-left">
<p>Alfredo Lanari nació en Mar del Plata, el 9 de febrero de 1910. Cursó la escuela primaria en el<span> </span><em>Colegio Champagnat</em>, donde continuó hasta segundo año del bachillerato. Ingresó al año siguiente al <a href="http://www.cnba.uba.ar/" target="_blank" rel="noopener">Colegio Nacional Buenos Aires</a>.</p>
<p>Ingresó a la <a href="http://www.uba.ar/" target="_blank" rel="noopener">Universidad de Buenos Aires</a>, <a href="http://www.fmed.uba.ar/" target="_blank" rel="noopener">Facultad de Medicina </a>en 1927, y fue elegido como representante de dicha universidad junto a<span> </span><em>Adalberto Goñi,</em><span> </span>para ser recibido en Londres por el príncipe de Gales en 1932.</p>
<p>Terminó sus estudios con medalla de oro en 1933.</p>
<p>En 1934 comenzó a trabajar en el<span> </span><strong>Servicio de Clínica Médica del Dr. Mariano Castex,</strong><span> </span>en el<span> </span><strong>Hospital de Clínicas</strong>, donde estuvo hasta 1943.</p>
<p>En 1936 se le había otorgado el premio Facultad a la mejor tesis sobre los transmisores químicos, realizada bajo la supervisión de Houssay, quizá el mayor maestro e inspirador de su vida.</p>
<p>Fue Walter Cannon el creador de la Fisiopatología moderna quien quizás lo decidiera a llevar su método científico y conocimientos a la medicina clínica.</p>
<p>Junto a otros colegas fundó la <a href="http://www.medicinabuenosaires.com/" target="_blank" rel="noopener">Revista Medicina</a> (Buenos Aires), que pronto ganó acceso a los centros de información internacionales.</p>
<p>En Buenos Aires el 28 de diciembre de 1948 se casó con Irene Tschudi con quien tuvo dos hijas. Ella, siendo bióloga, fue la primera en trabajar con cultivo mixto linfocitario en el Instituto.</p>
<p>En 1957 fue elegido<span> </span><em><strong>Profesor Titular de Medicina en la Universidad de Buenos Aires</strong><span> </span></em>y en 1958<span> </span><em><strong>Director del Instituto de Investigaciones Médicas de la Universidad de Buenos Aires</strong></em>, ambos con dedicación tiempo completo, trabajos que desempeñó hasta que se jubiló en 1976.</p>
<p>Falleció el 23 de Marzo de 1985, en el Instituto de Investigaciones Médicas, rodeado de su esposa, hijas, familiares, amigos, médicos, discípulos y enfermeras.</p>
<p>Para Lanari el Instituto fue la oportunidad de aplicar su espíritu creativo, desde las Residencias Médicas hasta el entusiasmo de la gente por investigar y sembrar… esperando la época de la cosecha.</p>
<p>Lanari fue un ejemplo del dicho de Ortega y Gasset:<em><span> </span>"Un auténtico profesor universitario no va a la Universidad, está en la Universidad».</em></p>
