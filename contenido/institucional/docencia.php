				<ul class="lista">
				<li><b>Metodolog&iacute;a</b>&nbsp;<b>de abordaje de la investigaci&oacute;n</b>(Desarrolle en 2 carillas como m&aacute;ximo).</li>
				</ul>
				<p>Describir, seg&uacute;n corresponda al tipo de proyecto, el dise&ntilde;o experimental, o el procedimiento, para la recolecci&oacute;n de informaci&oacute;n y su procesamiento. Es aconsejable la descripci&oacute;n muy breve de la metodolog&iacute;a de los fundamentos de la misma a usar dentro de cada secci&oacute;n donde se describen las tareas. Deber&aacute;n incluirse las variables principales, su an&aacute;lisis y descripci&oacute;n respecto del manejo de los resultados emergentes.</p>
				<ul class="lista">
				<li><b>Manejo estad&iacute;stico de los resultados.</b></li>
				</ul>
				<p>Cuando sea pertinente se deber&aacute; incluir una descripci&oacute;n del an&aacute;lisis estad&iacute;stico apropiado para la correcta interpretaci&oacute;n de los resultados obtenidos.</p>
				<ul class="lista">
				<li><b>Impacto y transferencia de los resultados</b>&nbsp;(m&aacute;ximo una carilla).</li>
				</ul>
